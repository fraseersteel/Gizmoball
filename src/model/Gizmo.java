package model;

public class Gizmo {

    private double xLocation;
    private double yLocation;


    public Gizmo(double x, double y) {
        this.xLocation = x;
        this.yLocation = y;
    }

    public double getxLocation() {
        return xLocation;
    }

    public double getyLocation() {
        return yLocation;
    }
}
